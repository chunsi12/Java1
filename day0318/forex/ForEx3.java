package day0318.forex;

public class ForEx3 {

	public static void main(String[] args) {
		// for 증감식
		// 0 ~ 10까지 2씩 증가하는 for 문
		for(int i = 0; i <11 ; i += 2) {
			System.out.println(i);
		}
		//10 ~ 1까지 1씩 감소하는 for문
		for(int j = 10; j <=1; j --) {
			System.out.println("j: "+j);
		}
        //"hello" 5번 출력
		for (int h = 0; h<5; h++) {
			System.out.println("hello");
		}
	}

}
